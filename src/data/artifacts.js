export const clampArtifactLevel = (level) => {
    if (!Number.isFinite(level)) return 0;
    return Math.max(0, Math.floor(level));
};

export const getArtifactEffectValue = (artifact, level = 0) => {
    if (!artifact) return 0;
    const normalized = clampArtifactLevel(level);
    if (normalized <= 0) return 0;
    const additionalLevels = Math.max(0, normalized - 1);
    const base = Number.isFinite(artifact.baseEffect) ? artifact.baseEffect : 0;
    const growth = Number.isFinite(artifact.effectGrowth) ? artifact.effectGrowth : 0;
    return base + additionalLevels * growth;
};

export const getArtifactDamageValue = (artifact, level = 0) => {
    if (!artifact) return 0;
    const normalized = clampArtifactLevel(level);
    if (normalized <= 0) return 0;
    const base = Number.isFinite(artifact.baseArtifactDamage) ? artifact.baseArtifactDamage : 0;
    const growth = Number.isFinite(artifact.artifactDamageGrowth) ? artifact.artifactDamageGrowth : 0;
    const additionalLevels = Math.max(0, normalized - 1);
    return base + additionalLevels * growth;
};

export const getArtifactUpgradeCost = (artifact, level = 0) => {
    if (!artifact) return Infinity;
    const normalized = clampArtifactLevel(level);
    const exponent = Math.max(0, normalized);
    const baseCost = Number.isFinite(artifact.baseCost) ? Math.max(1, Math.floor(artifact.baseCost)) : 1;
    const growth = Number.isFinite(artifact.costGrowth) ? Math.max(1, artifact.costGrowth) : 1;
    return Math.max(1, Math.floor(baseCost * Math.pow(growth, exponent)));
};

export const formatArtifactEffectValue = (artifact, value) => {
    if (!artifact) return 'íš¨ê³¼ ì—†ìŒ';
    if (!Number.isFinite(value)) return 'íš¨ê³¼ ì—†ìŒ';
    if (artifact.format === 'percent') {
        return `${(value * 100).toFixed(1)}%`;
    }
    return value.toLocaleString('ko-KR');
};

export const formatArtifactDamageValue = (value) => {
    if (!Number.isFinite(value) || value <= 0) return '+0.0%';
    return `+${(value * 100).toFixed(1)}%`;
};

export const getArtifactDiscoveryCost = (discoveredCount = 0) => {
    const { baseDiscoveryCost, discoveryGrowth } = ARTIFACT_DISCOVERY_CONFIG;
    const base = Math.max(1, Number(baseDiscoveryCost) || 1);
    const growth = Math.max(1, Number(discoveryGrowth) || 1);
    return Math.max(1, Math.floor(base * Math.pow(growth, Math.max(0, discoveredCount))));
};

export const getArtifactSalvageCost = (discoveredCount = 0) => {
    const { salvageBaseCost, salvageCostPerDiscovery } = ARTIFACT_DISCOVERY_CONFIG;
    const base = Math.max(0, Number(salvageBaseCost) || 0);
    const step = Math.max(0, Number(salvageCostPerDiscovery) || 0);
    return Math.max(0, Math.floor(base + step * Math.max(0, discoveredCount - 1)));
};
